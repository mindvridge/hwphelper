pset = hwp.HParameterSet.HFindReplace


def rep(old, new):
    """전체 찾아바꾸기."""
    hwp.HAction.GetDefault("AllReplace", pset.HSet)
    pset.FindString = old
    pset.ReplaceString = new
    pset.IgnoreMessage = 1
    pset.FindType = 1
    r = hwp.HAction.Execute("AllReplace", pset.HSet)
    status = "OK" if r else "--"
    print(f"  {status}: {old[:40]} -> {new[:40]}")


